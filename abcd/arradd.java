import java.util.Scanner;
public class arradd {
public static void main (String args[]){
	int[] arr = new int[10];
	Scanner scan = new Scanner(System.in);
	int count =0;
	for (int i =0;i<10;i++)
	{
		arr[i] = scan.nextInt();
	}
	
	
	for (int j : arr){
		count = count + j;
	}
	System.out.println(count);
}
}
